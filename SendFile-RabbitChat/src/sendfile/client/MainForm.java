    private void sendFileMenuActionPerformed(java.awt.event.ActionEvent evt) {
       
        if(!attachmentOpen){
            SendFile s = new SendFile();
            if(s.prepare(username, host, port, this)){
                s.setLocationRelativeTo(null);
                s.setVisible(true);
                attachmentOpen = true;
            } else {
                SoundEffect.ErrorSound.play();
                JOptionPane.showMessageDialog(this, "Cannot establish File Sharing at this time, please try again later!", "error", JOptionPane.ERROR_MESSAGE);

            }
        }
    }

